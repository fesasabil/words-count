function wordsnew(kosakata) {
  return kosakata.split(" ").lenght;
}

console.log(wordsnew('I life at home')); // 4
console.log(wordsnew('I wanna go to home')); // 5
console.log(wordsnew('My mother in kitchen')); // 4
console.log(wordsnew('I am')); // 2
console.log(wordsnew('I believe I can fly')); // 5
